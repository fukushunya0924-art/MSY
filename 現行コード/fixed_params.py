"""
Catch-MSY で確定した外挿パラメータの単一の真実の源。

capacity_ry モデル（12変数）のうち、マイワシ・ウルメイワシの自然増殖率 r_x と
ブリ・サワラの捕食→捕食者変換効率の和 S(=c+d) の4値は、ODE推定とは別手法の
Catch-MSY で NLM/LM 共通の値としてあらかじめ推定済みである。
`model_constrained.py` はこれらを固定値として外挿し、残り8自由変数
（r_y1, r_y2, L11, L12, L21, L22, theta1, theta2）だけを ODE 推定する。

データ源（2026-07-14改訂: catch源をODEと整合させた）:
  従来は全4値を e-stat 太平洋12県漁獲量から推定していたが、ODE推定は
  FRA資源評価（太平洋系群）の biomass / catch を使うため、「Catch-MSYと
  ODEで漁獲量データが異なる」という批判を避けるべく、FRA資源評価に catch が
  存在する3種（マイワシ・ブリ・サワラ）は FRA catch で再推定した値に差し替えた。
    - マイワシ r_x1 : FRA資源評価 catch 1975-2024
    - ブリ   S1    : FRA資源評価 catch 1994-2024
    - サワラ  S2    : FRA資源評価 catch 1987-2024
    - ウルメ  r_x2   : e-stat 太平洋12県 catch（FRA に絶対 catch が無く、ODE側も
                      ウルメの catch は e-stat 12県を使うため、両手法で整合済み）
  感度解析（e-stat catch版 との比較）: r_x1 と S1 は catch 源をe-stat↔FRAに
  替えても ±5% でほぼ不変（source-invariant, 相関 マイワシ0.99/ブリ0.91）。
  S2（サワラ）のみ 0.361→0.260 と変化するが、サワラの総MSY寄与は<1%で結論に
  影響しない（相関0.44の弱点は開示した上で採用）。

キー対応（capacity_ry の names と対応させると r_x1, r_x2 はそのまま、
S1, S2 は c1+d1, c2+d2 に相当）:
  r_x1  : マイワシの自然増殖率 (1/年)
  r_x2  : ウルメイワシの自然増殖率 (1/年)
  S1    : ブリの c1+d1（マイワシ・ウルメイワシ由来の変換効率の和）
  S2    : サワラの c2+d2（同上）

これら4値はレジーム（NLM/LM）に依らず共通の値として扱う。

注意（r_x1 について、要教授相談）:
  r_x1 は、他の3値が終端レンジ選定の標準ルール通り（[0.01,0.4] や [0.3,0.7]
  の規定レンジ）で確定したのに対し、標準ルールを外れた [0.6, 0.95] という
  高いレンジを例外的に採用して得られた暫定値である。FRA catch に替えても
  終端/max=0.47 だが標準低レンジ[0.01,0.4]では生存ペア n=0 となり、この例外は
  データ源の問題ではなくマイワシのブーム・バスト性に起因して残る。採用の
  妥当性は未確定で教授相談待ち。r_x2, S1, S2 は標準ルール通りに確定済み。
"""
from typing import Dict, Tuple

# 点推定値（1/年）。キーは model_constrained.reconstruct_params の fixed 引数と対応。
_POINT: Dict[str, float] = {
    "r_x1": 0.940,  # マイワシ。FRA catch 1975-2024。終端レンジ[0.6,0.95]（標準ルール外・暫定・要相談）
    "r_x2": 0.739,  # ウルメイワシ。e-stat太平洋12県 catch（ODEと整合）。終端レンジ[0.01,0.4]（標準ルール通り）
    "S1":   0.395,  # ブリ c1+d1。FRA catch 1994-2024。終端レンジ[0.3,0.7]（標準ルール通り）
    "S2":   0.260,  # サワラ c2+d2。FRA catch 1987-2024。終端レンジ[0.01,0.4]（標準ルール通り）
}

# 信頼区間 (lo, hi)。Catch-MSY 生存ペアの 25-75% 点。
_CI: Dict[str, Tuple[float, float]] = {
    "r_x1": (0.746, 1.180),
    "r_x2": (0.642, 0.824),
    "S1":   (0.268, 0.569),
    "S2":   (0.220, 0.295),
}


def get_point() -> Dict[str, float]:
    """固定パラメータの点推定値を dict で返す（キー: r_x1, r_x2, S1, S2）。"""
    return dict(_POINT)


def get_ci(name: str) -> Tuple[float, float]:
    """指定パラメータ名（r_x1, r_x2, S1, S2 のいずれか）の信頼区間 (lo, hi) を返す。"""
    return _CI[name]
